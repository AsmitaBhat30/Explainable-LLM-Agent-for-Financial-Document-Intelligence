"""Run comprehensive evaluation on the system.
Usage: python -m scripts.run_evaluation [--fresh]

Requires OPENAI_API_KEY to be set (see .env.example) — answers are
generated by a real LLM call and scored against the retrieved context, so
there is no stubbed/fake-answer fallback here.

Exercises the actual agent pipeline end to end: a FaissVectorStore is
built from every context chunk across the golden + adversarial datasets
(so retrieval has real distractors to sort through, not just the one gold
chunk for each query), then each query runs through
RetrieverAgent -> ComplianceAgent -> ExplanationAgent for real.

Resumable: each case's result is written to evaluation/results/checkpoint.json
(see evaluation/checkpoint.py) immediately after it succeeds. If the run
dies partway through -- most commonly an OpenAI rate limit or quota error
on a 38-query dataset -- re-running this script picks up exactly where it
left off instead of re-querying (and re-billing) every case from scratch.
Once every case has a result, the run is archived to a timestamped file
under evaluation/results/ and the checkpoint is cleared, so a subsequent
invocation starts a new run. Pass --fresh to discard an in-progress
checkpoint and start over.
"""

import argparse
import logging
import os
import sys
from typing import Dict, List, Optional

from openai import OpenAI
from sentence_transformers import SentenceTransformer

from agents.compliance_agent import ComplianceAgent
from agents.explanation_agent import ExplanationAgent
from agents.retriever_agent import RetrieverAgent
from evaluation.checkpoint import RunCheckpoint
from evaluation.metrics import EvaluationMetrics
from evaluation.test_cases import AdversarialTestSuite, TestSetGenerator
from indexing.vector_store import FaissVectorStore

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

COVERED_REGULATIONS = [
    "SEC EDGAR 10-K",
    "MiFID II",
    "PSD2",
    "GDPR",
    "CRR",
    "BaFin",
    "Basel III",
    "ECB Supervisory Manual",
]


def _build_llm_client() -> Optional[OpenAI]:
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        return None
    return OpenAI(api_key=api_key)


def _build_vector_store(
    all_cases: List[Dict], embedder: SentenceTransformer
) -> FaissVectorStore:
    """Pool every golden case's context chunks into one corpus so
    retrieval has to find the right chunk among real distractors, instead
    of each query being handed only its own gold chunk.
    """
    chunks = [chunk for case in all_cases for chunk in case.get("context_chunks", [])]
    dim = embedder.get_sentence_embedding_dimension()
    store = FaissVectorStore(dim=dim)
    if not chunks:
        return store
    texts = [c["text"] for c in chunks]
    embeddings = embedder.encode(texts, convert_to_numpy=True)
    store.add(chunks, embeddings)
    return store


def _run_case(
    retriever: RetrieverAgent,
    compliance_agent: ComplianceAgent,
    explanation_agent: ExplanationAgent,
    embedder: SentenceTransformer,
    case: Dict,
) -> Dict:
    query = case["query"]
    query_embedding = embedder.encode([query], convert_to_numpy=True)[0]

    retrieval = retriever.execute({"query": query, "query_embedding": query_embedding})
    retrieved_chunks = retrieval["retrieved_chunks"]

    compliance = compliance_agent.execute(
        {"query": query, "retrieved_chunks": retrieved_chunks}
    )

    # This is the only step that calls the real LLM API, and therefore the
    # only step that can hit a rate limit / quota error mid-run.
    explanation = explanation_agent.execute(
        {
            "query": query,
            "retrieved_chunks": retrieved_chunks,
            "compliance": compliance,
            "retrieval_confidence": retrieval["confidence"],
        }
    )

    return {
        "name": case["name"],
        "answer": explanation["answer"],
        "citations": explanation["citations"],
        "context_chunks": retrieved_chunks,
        "predicted_risk_level": compliance["risk_level"],
        "gold_risk_level": case.get("compliance", {}).get("risk_level"),
    }


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--fresh",
        action="store_true",
        help=(
            "Discard any in-progress checkpoint under evaluation/results/ "
            "and start a brand new run instead of resuming it."
        ),
    )
    args = parser.parse_args()

    logger.info("=" * 60)
    logger.info("SYSTEM EVALUATION")
    logger.info("=" * 60)

    llm_client = _build_llm_client()
    if llm_client is None:
        logger.error(
            "OPENAI_API_KEY is not set — cannot generate answers to "
            "evaluate. Set it in your environment (see .env.example) and "
            "re-run. Not falling back to a fake answer generator."
        )
        return

    model = os.getenv("OPENAI_MODEL", "gpt-4o-mini")
    explanation_agent = ExplanationAgent(llm_client, model=model)
    compliance_agent = ComplianceAgent(regulations=COVERED_REGULATIONS)

    logger.info("Loading embedding model for retrieval and faithfulness scoring...")
    embedder = SentenceTransformer("sentence-transformers/all-MiniLM-L6-v2")
    metrics = EvaluationMetrics(
        embed_fn=lambda texts: embedder.encode(texts, convert_to_numpy=True)
    )

    golden_cases = TestSetGenerator.get_golden_dataset()
    adversarial_cases = AdversarialTestSuite.get_test_cases()
    all_cases = golden_cases + adversarial_cases

    logger.info("Building in-memory FAISS index from pooled context chunks...")
    vector_store = _build_vector_store(golden_cases, embedder)
    retriever = RetrieverAgent(vector_store, top_k=5)
    logger.info(f"Indexed {vector_store.ntotal} chunks")

    checkpoint = RunCheckpoint()
    if args.fresh:
        checkpoint.discard()
        logger.info("--fresh passed: discarded any in-progress checkpoint.")
    elif len(checkpoint) > 0:
        logger.info(
            f"Resuming previous run: {len(checkpoint)}/{len(all_cases)} "
            f"cases already completed. Re-querying only the rest."
        )

    logger.info(
        f"\nTotal cases: {len(all_cases)} "
        f"({len(golden_cases)} golden, {len(adversarial_cases)} adversarial)"
    )
    logger.info("-" * 60)

    for i, case in enumerate(all_cases, start=1):
        if case["name"] in checkpoint:
            continue
        logger.info(f"[{i}/{len(all_cases)}] {case['name']}")
        try:
            result = _run_case(
                retriever, compliance_agent, explanation_agent, embedder, case
            )
        except KeyboardInterrupt:
            logger.warning(
                f"Interrupted. Progress saved: {len(checkpoint)}/{len(all_cases)} "
                f"cases. Re-run `python -m scripts.run_evaluation` to continue."
            )
            sys.exit(130)
        except Exception:
            logger.exception(
                f"Evaluation stopped on case {case['name']!r} — likely an "
                f"OpenAI rate limit or quota error. Progress saved: "
                f"{len(checkpoint)}/{len(all_cases)} cases. Re-run "
                f"`python -m scripts.run_evaluation` (no flags) to continue "
                f"from here once the quota/rate limit clears; already-"
                f"completed cases will not be re-queried."
            )
            sys.exit(1)
        checkpoint.record(case["name"], result)

    logger.info(f"\nAll {len(all_cases)} cases complete.")

    metrics.results = [checkpoint.results[case["name"]] for case in all_cases]
    report = metrics.generate_report()

    labeled = [r for r in metrics.results if r["gold_risk_level"] is not None]
    compliance_accuracy = (
        sum(1 for r in labeled if r["predicted_risk_level"] == r["gold_risk_level"])
        / len(labeled)
        if labeled
        else None
    )
    report["compliance_accuracy"] = compliance_accuracy

    logger.info("\n" + "=" * 60)
    logger.info("RESULTS")
    logger.info("=" * 60)
    for key, value in report.items():
        logger.info(f"{key}: {value}")

    logger.info("\nPer-case faithfulness:")
    for r in metrics.results:
        faithfulness = metrics.calculate_faithfulness(r["answer"], r["context_chunks"])
        flag = (
            "OK" if not faithfulness["unsupported_sentences"] else "UNSUPPORTED CLAIMS"
        )
        logger.info(
            f"  [{flag}] {r['name']}: "
            f"faithfulness={faithfulness['faithfulness_score']:.2f}"
        )
        for sentence in faithfulness["unsupported_sentences"]:
            logger.info(f"      - not grounded in context: {sentence!r}")

        if r["gold_risk_level"] is not None and (
            r["predicted_risk_level"] != r["gold_risk_level"]
        ):
            logger.info(
                f"      - compliance mismatch: predicted="
                f"{r['predicted_risk_level']!r}, gold={r['gold_risk_level']!r}"
            )

    archive_path = checkpoint.archive_and_clear(report)
    logger.info(f"\nRun archived to {archive_path}")
    logger.info(
        "evaluation/results/latest.json also updated -- compare against it "
        "next time to track regressions, per .claude/skills/evaluate.md."
    )


if __name__ == "__main__":
    main()
